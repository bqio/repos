export type Language = "ru" | "en"

export function getItemsLabel(count: number, lang: Language): string {
  if (lang === "en") return count === 1 ? translations.en.item : translations.en.items

  if (count % 10 === 1 && count % 100 !== 11) return translations.ru.item
  if (count % 10 >= 2 && count % 10 <= 4 && (count % 100 < 10 || count % 100 >= 20)) return translations.ru.items2
  return translations.ru.items
}
